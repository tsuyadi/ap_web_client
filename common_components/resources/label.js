export function LoadLabel(name){

    var lang = localStorage.getItem('LANG');

    if(lang == "" || lang == undefined) lang = 'EN';

    try{
        switch(lang){
            case 'EN' : return EN_LANG[name];
            case 'ID' : return ID_LANG[name];
        }
    }catch(e){
        return 'N/A';
    }

}